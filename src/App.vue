    toolbars,
  },
  created() {
    this.token = this.$store.getters.getToken;
    this.offlineControl();
  },
  mounted() {
    this.loadScriptsByRoute(this.$route.path); // ilk alta da alsn
  },
  watch: {
    "$route.path"(newPath) {
      this.loadScriptsByRoute(newPath); // route deiince ilgili scriptleri ykle
    },
  },
  methods: {
    offlineControl() {
      setInterval(() => {
        const divOff = document.getElementById("offline");
        const divOn = document.getElementById("online");
        if (!divOff || !divOn) return;
        window.addEventListener("online", () => {
          divOff.style.display = "none";
          divOn.style.display = "block";
        });
        window.addEventListener("offline", () => {
          divOff.style.display = "block";
          divOn.style.display = "none";
        });
      }, 3000);
    },

    loadScript(src) {
      if (this.loadedScripts.has(src)) return; // zaten yklendiyse tekrar ykleme

      const script = document.createElement("script");
      script.src = src;
      script.async = true;
      script.onload = () => {
        this.loadedScripts.add(src);
        console.log(` Script yklendi: ${src}`);
      };
      script.onerror = () => {
        console.warn(` Script yklenemedi: ${src}`);
      };
      document.head.appendChild(script);
    },

    loadScriptsByRoute(path) {
      // PATH'e gre hangi script yklenecek?
      if (path === "/login") {
        this.loadScript("_assets/js/custom/authentication/sign-in/general.js");
      } else if (path === "/register") {
